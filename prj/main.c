#include <stdio.h>
#include <string.h>
#include <ctype.h>
#include <locale.h> // Для корректной кодировки

int main() {
    setlocale(LC_ALL, ""); // Устанавливаем локаль системы

    // Оголошення змінних
    char word[101]; // Массив для слова (до 100 символов + \0)
    int count_pa = 0; // Лічильник входжень "pa"
    int length = 0; // Довжина слова
    int uppercase_count = 0; // Лічильник заголовних літер

    // Виведення інформації про програму
    printf("Програма для аналізу слова.\n");
    printf("Введіть слово (закінчується #): ");

    // Отримання введеного слова (читает строку с пробелами, но останавливается на '#')
    scanf("%100[^#]", word);
    getchar(); // Убираем '#' из потока ввода

    // Видалення зайвих пробілів (якщо є)
    int i;
    for (i = 0; word[i] != '\0'; i++) {
        if (word[i] == ' ') {
            word[i] = '\0';
            break;
        }
    }

    // Підрахунок входжень "pa"
    for (i = 0; word[i] != '\0'; i++) {
        if (word[i] == 'p' && word[i + 1] == 'a') {
            count_pa++;
        }
    }

    // Якщо знайдено "pa", виводимо його кількість
    if (count_pa > 0) {
        printf("Кількість складів 'pa' у слові: %d\n", count_pa);
    } else {
        // Якщо "pa" немає, обчислюємо довжину та кількість заголовних літер
        length = strlen(word);

        // Підрахунок заголовних літер
        for (i = 0; word[i] != '\0'; i++) {
            if (isupper((unsigned char)word[i])) {
                uppercase_count++;
            }
        }

        // Виведення результатів
        printf("Довжина слова: %d\n", length);
        printf("Кількість заголовних літер: %d\n", uppercase_count);
    }

    return 0;
}
